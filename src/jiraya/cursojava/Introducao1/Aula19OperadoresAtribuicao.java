package jiraya.cursojava.Introducao1;

public class Aula19OperadoresAtribuicao {
    /* = += -= *= /= %=
    https://youtu.be/EFrq1WxsL1k?list=PL62G310vn6nFIsOCC0H-C2infYgwm8SWW
     */
    public static void main(String[] args) {
        double bonus = 1000;
        bonus += 1000;
        bonus -= 500;
        bonus *= 2;
        bonus /= 4;

        System.out.println(bonus);

        int contador = 0;
        contador ++;
        System.out.println(contador);
    }


}
