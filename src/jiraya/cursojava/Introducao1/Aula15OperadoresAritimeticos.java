package jiraya.cursojava.Introducao1;

public class Aula15OperadoresAritimeticos {
    // + - * / (soma, subtração, multipliação e divisão

    public static void main(String[] args) {
        int numero1 = 10;
        int numero2 = 20;

        System.out.println(numero2-numero1);
        System.out.println(numero2+numero1);
        System.out.println(numero2*numero1);
        System.out.println(numero2/numero1);

    }
}
